#include "./includes/libft.h"

int		ft_nisdigit(char *s, int n)
{
	int i;

	i = 0;
	if (n > (int)ft_strlen(s))
		return 0;
	if (s && n > 0)
	{
		while (i < n)
		{
			if (s[i] < '0' || s[i] > '9')
				return (0);
			++i;
		}
	}
	return (1);
}
