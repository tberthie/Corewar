#include "./includes/libft.h"

void		ft_strdel(char **as)
{
	int		i;
	char	*new;

	i = 0;
	if (as && *as)
	{
		new = *as;
		while (new[i])
		{
			free(new + i);
			i++;
		}
		*as = NULL;
	}
}
