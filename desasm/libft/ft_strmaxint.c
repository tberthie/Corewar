#include "./includes/libft.h"

int			ft_strmaxint(const char *str)
{
	int		i;
	char	*s;

	i = 0;
	s = (char *)str;
	while (ft_isdigit(s[i]))
		++i;
	if (i > 10)
		return (0);
	if (i < 10 || (i == 10 && s[0] <= '2' && s[1] <= '1'
		&& s[2] <= '4' && s[3] <= '7' && s[4] <= '4' && s[5] <= '8' && s[6]
		<= '3' && s[7] <= '6' && s[8] <= '4' && s[9] <= '7'))
		return (1);
	return (0);
}
